    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(lstEntiteti.getSelectedValue() == null){
            return;
        }
        obrada.setEntitet(lstEntiteti.getSelectedValue());
        try {
            obrada.delete();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPromijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromijeniActionPerformed
        if(lstEntiteti.getSelectedValue()==null){
            return;
        }
        var e = lstEntiteti.getSelectedValue();
        obrada.setEntitet(e);
        
        List<Racunalo> racunala = new ArrayList<>();
        DefaultListModel<Racunalo> m = (DefaultListModel<Racunalo>) lstRacunalaOsobe.getModel();
        for(int i=0;i<m.size();i++){
            racunala.add(m.get(i));
        }
        
        obrada.setPodaci(txtIme.getText(), txtPrezime.getText(), (Mjesto)cmbMjesto.getSelectedItem(), racunala);
        try {
            obrada.update();
            ucitajPodatke();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnPromijeniActionPerformed

    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        obrada.setNew();
        List<Racunalo> racunala = new ArrayList<>();
        DefaultListModel<Racunalo> r = (DefaultListModel<Racunalo>) lstRacunalaOsobe.getModel();
        for(int i=0;i<r.size();i++){
            racunala.add(r.get(i));
        }        
        
        obrada.setPodaci(txtIme.getText(), txtPrezime.getText(), (Mjesto)cmbMjesto.getSelectedItem(), racunala);

        try {
            obrada.create();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnKreirajActionPerformed

    private void btnDodajRacunaloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajRacunaloActionPerformed
        if(lstRacunalaUBazi.getSelectedValue() == null){
            return;
        }
        ((DefaultListModel<Racunalo>)lstRacunalaOsobe.getModel()).addElement(lstRacunalaUBazi.getSelectedValue());
        lstRacunalaOsobe.repaint();
    }//GEN-LAST:event_btnDodajRacunaloActionPerformed

    private void btnIzbrisiRacunaloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrisiRacunaloActionPerformed
        if(lstRacunalaOsobe.getSelectedValue()==null){
            return;
        }
        
        ((DefaultListModel<Racunalo>)lstRacunalaOsobe.getModel()).removeElement(lstRacunalaOsobe.getSelectedValue());
        lstRacunalaOsobe.repaint();
    }//GEN-LAST:event_btnIzbrisiRacunaloActionPerformed

    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if(evt.getValueIsAdjusting() || lstEntiteti.getSelectedValue() == null){
            return;
        }
        txtIme.setText(lstEntiteti.getSelectedValue().getIme());
        txtPrezime.setText(lstEntiteti.getSelectedValue().getPrezime());
        cmbMjesto.setSelectedItem(lstEntiteti.getSelectedValue().getMjesto());
        DefaultListModel<Racunalo> m = new DefaultListModel<>();
        m.addAll(lstEntiteti.getSelectedValue().getRacunala());
        lstRacunalaOsobe.setModel(m);
        lstRacunalaOsobe.repaint();
    }//GEN-LAST:event_lstEntitetiValueChanged


